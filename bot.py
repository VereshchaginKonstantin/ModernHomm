            response = self._format_search_results(username, messages, total_count, page)

            # –°–æ–∑–¥–∞–µ–º –∫–Ω–æ–ø–∫–∏ –ø–∞–≥–∏–Ω–∞—Ü–∏–∏
            keyboard = self._create_pagination_keyboard(username, page, total_count)

